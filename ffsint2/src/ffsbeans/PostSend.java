package ffsbeans;

public class PostSend {

    private String tranId;
    private String lidNo;
    private String recBy;
    private String whatSend;
    
    public PostSend() {

    }

    public String gettranId() {
        return tranId;
    }

    public void settranId(String tranId) {
        this.tranId = tranId;
    }

    public String getlidNo() {
        return lidNo;
    }

    public void setlidNo(String lidNo) {
        this.lidNo = lidNo;
    }

    public String getrecBy() {
        return recBy;
    }

    public void setrecBy(String recBy) {
        this.recBy = recBy;
    }

    public String getwhatSend() {
        return whatSend;
    }

    public void setwhatSend(String whatSend) {
        this.whatSend = whatSend;
    }

    
}
